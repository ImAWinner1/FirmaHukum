/**
 * Fungsi utilitas untuk mengambil data area praktik spesifik berdasarkan slug.
 * Mengembalikan nilai undefined jika tidak ada data yang cocok.
 */
export function getPracticeAreaBySlug(slug: string): PracticeArea | undefined {
  return practiceAreas.find((area) => area.slug === slug);
}

/**
 * Fungsi untuk mengambil semua slug area praktik. 
 * Umumnya digunakan untuk proses pemetaan rute statis (static path generation).
 */
export function getAllPracticeAreaSlugs(): string[] {
  return practiceAreas.map((area) => area.slug);
}
